    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Delete Button
        if(vid.getText().isEmpty() || vloc.getText().isEmpty() || vbudget.getText().isEmpty() || vdes.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Information Missing");
        }
        else
        {
            try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/wedding_management","root","12345");
            String id = vid.getText();
            String deletequery = "delete from venue where v_id="+id;
            Statement Add = con.createStatement();
            Add.executeUpdate(deletequery);
            JOptionPane.showMessageDialog(this,"Venue Record Deleted");
            venue_load();
            clear();

        } catch (SQLException ex) {
            Logger.getLogger(admenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

